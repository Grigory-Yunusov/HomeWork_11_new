# Скласти програму, яка вводить цілі k, l.
# Якщо вони не рівні, то менше з них замінюється більшим, інакше обидва замінюються на 0.
k = int(input("Введіть число k: "))
l = int(input("Введіть число l: "))

if k != l:
    if k < l:
        k = l
    else:
        l = k
else:
    l = 0
    k = 0
print(k, l)